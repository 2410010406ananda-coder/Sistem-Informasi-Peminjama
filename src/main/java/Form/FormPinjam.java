    private void tbpinjamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbpinjamMouseClicked
       
     int baris = tbpinjam.getSelectedRow();

    if(baris > -1){

        tfid.setText(
                tbpinjam.getValueAt(baris, 0).toString()
        );

        tfpeminjam.setText(
                tbpinjam.getValueAt(baris, 1).toString()
        );

        tftanggal.setText(
                tbpinjam.getValueAt(baris, 2).toString()
        );
    }     // TODO add your handling code here:
    }//GEN-LAST:event_tbpinjamMouseClicked

    private void bttambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttambahActionPerformed
        if(tfpeminjam.getText().trim().isEmpty()){

        JOptionPane.showMessageDialog(
                null,
                "Nama peminjam tidak boleh kosong!"
        );

        return;
    }

    ModelPinjam mp = new ModelPinjam();

    mp.setPeminjam(
            tfpeminjam.getText()
    );

    mp.setTanggal(
            tftanggal.getText()
    );

    mp.setId_barang(
            Integer.parseInt(
                    cobarang.getSelectedItem().toString()
            )
    );

    controller.tambah(mp);

    tampilkanData();
    resetForm(); // TODO add your handling code here:
    }//GEN-LAST:event_bttambahActionPerformed

    private void bteditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bteditActionPerformed
     
    if(tfid.getText().isEmpty()){

        JOptionPane.showMessageDialog(
                null,
                "Pilih data terlebih dahulu!"
        );

        return;
    }

    ModelPinjam mp = new ModelPinjam();

    mp.setId_pinjam(
            Integer.parseInt(
                    tfid.getText()
            )
    );

    mp.setPeminjam(
            tfpeminjam.getText()
    );

    mp.setTanggal(
            tftanggal.getText()
    );

    mp.setId_barang(
            Integer.parseInt(
                    cobarang.getSelectedItem().toString()
            )
    );

    controller.edit(mp);

    tampilkanData();
    resetForm();  // TODO add your handling code here:
    }//GEN-LAST:event_bteditActionPerformed

    private void bthapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bthapusActionPerformed
         if(tfid.getText().isEmpty()){

        JOptionPane.showMessageDialog(
                null,
                "Pilih data terlebih dahulu!"
        );

        return;
    }

    int jawab = JOptionPane.showConfirmDialog(
            null,
            "Yakin hapus data?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION
    );

    if(jawab == JOptionPane.YES_OPTION){

        controller.hapus(
                tfid.getText()
        );

        tampilkanData();
        resetForm();
    }   // TODO add your handling code here:
    }//GEN-LAST:event_bthapusActionPerformed

