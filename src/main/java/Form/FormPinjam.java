private void resetForm() {
    tfid.setText("");
    tfpeminjam.setText("");
    tftanggal.setText("");

    if(cobarang.getItemCount() > 0){
        cobarang.setSelectedIndex(0);
    }
}

    private void loadComboBarang() {

      try {

        cobarang.removeAllItems();

        ResultSet rs = controller.tampilBarang();

        while(rs.next()) {

            cobarang.addItem(
                rs.getString("id_barang")
                + " - " +
                rs.getString("nama_barang")
            );
        }

    } catch(Exception e) {

        JOptionPane.showMessageDialog(
                null,
                e.getMessage()
        );
    }
}

