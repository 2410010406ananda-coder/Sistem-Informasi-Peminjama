    private void tbkembaliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbkembaliMouseClicked
        int baris = tbkembali.getSelectedRow();

    if(baris > -1){

        tfidcustomer.setText(
                tbkembali.getValueAt(baris,0).toString()
        );

        tftanggal.setText(
                tbkembali.getValueAt(baris,3).toString()
        );
    }       // TODO add your handling code here:
    }//GEN-LAST:event_tbkembaliMouseClicked

    private void bttambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttambahActionPerformed
       
    if(tftanggal.getText().trim().isEmpty()){

        JOptionPane.showMessageDialog(
                null,
                "Tanggal tidak boleh kosong!"
        );

        return;
    }

    ModelKembali mk = new ModelKembali();

    mk.setTanggal(
            tftanggal.getText()
    );

    int idPinjam = Integer.parseInt(
            copinjam.getSelectedItem()
            .toString()
            .split(" - ")[0]
    );

    mk.setId_pinjam(idPinjam);

    controller.tambah(mk);

    tampilkanData();
    resetForm();      // TODO add your handling code here:
    }//GEN-LAST:event_bttambahActionPerformed

    private void bteditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bteditActionPerformed
    
    if(tfidcustomer.getText().isEmpty()){

        JOptionPane.showMessageDialog(
                null,
                "Pilih data terlebih dahulu!"
        );

        return;
    }

    ModelKembali mk = new ModelKembali();

    mk.setId_kembali(
            Integer.parseInt(
                    tfidcustomer.getText()
            )
    );

    mk.setTanggal(
            tftanggal.getText()
    );

    int idPinjam = Integer.parseInt(
            copinjam.getSelectedItem()
            .toString()
            .split(" - ")[0]
    );

    mk.setId_pinjam(idPinjam);

    controller.edit(mk);

    tampilkanData();
    resetForm(); // TODO add your handling code here:
    }//GEN-LAST:event_bteditActionPerformed

    private void bthapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bthapusActionPerformed
      
    if(tfidcustomer.getText().isEmpty()){

        JOptionPane.showMessageDialog(
                null,
                "Pilih data terlebih dahulu!"
        );

        return;
    }

    int jawab = JOptionPane.showConfirmDialog(
            null,
            "Yakin hapus data?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION
    );

    if(jawab == JOptionPane.YES_OPTION){

        controller.hapus(
                tfidcustomer.getText()
        );

        tampilkanData();
        resetForm();
    }      // TODO add your handling code here:
    }//GEN-LAST:event_bthapusActionPerformed

