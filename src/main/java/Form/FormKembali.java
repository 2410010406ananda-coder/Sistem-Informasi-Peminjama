    private void tampilComboPinjam() {

    try {

        copinjam.removeAllItems();

        ResultSet rs =
        cp.cn.createStatement().executeQuery(
        "SELECT id_pinjam,peminjam FROM tb_pinjam");

        while(rs.next()){

            copinjam.addItem(
                rs.getString("id_pinjam")
                +" - "+
                rs.getString("peminjam")
            );
        }

    } catch(Exception e){

        JOptionPane.showMessageDialog(
                null,
                e.getMessage()
        );
    }
}

